namespace TopMarksDevelopment.ExpressionBuilder;

/// <summary>
/// Represents logical connectors that can be used in expressions.
/// </summary>
public enum Connector
{
    /// <summary>
    /// Represents the logical AND operator.
    /// </summary>
    And = 0,

    /// <summary>
    /// Represents the logical OR operator.
    /// </summary>
    Or = 1,
}
